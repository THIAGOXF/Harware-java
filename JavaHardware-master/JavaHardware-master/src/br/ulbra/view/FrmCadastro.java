    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        if(txtItem.getText().equals("") || txtItem.getText().equals(null)) //check if empty
        {
            JOptionPane.showMessageDialog(null, "Campo vazio"); //out insucess            
        }
        else
        {
            bditems.adicionar(txtItem.getText());
            
            txtLista.setText(bditems.listarHards()); //list
            
            txtItem.setText(null);
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

