    private void btnPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarActionPerformed
        String nome = txtPesquisa.getText(); //variables
        int i = bditems.pesquisar(nome);
        
        if(i != -1)
        {
            txtLista.setText("Resultado da pesquisa:\n\n=================================\n\n" + (i + 1) + " - " + nome.toUpperCase()); //out sucess 
            txtPesquisa.setText(null); //clears field
        }
        else if(bditems.returnVazio()) //check if empty
        {
            JOptionPane.showMessageDialog(null, "Base vazia"); //out if empty
        }
        else
        {
            JOptionPane.showMessageDialog(null, "Item nao encontrado"); //out insucess
        }
    }//GEN-LAST:event_btnPesquisarActionPerformed

    private void btnOptionsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOptionsActionPerformed
        if(!bditems.returnVazio()) //check if empty
        {
            pnEdit.setVisible(true);
        
            lblEdit.setVisible(true);
            txtEditar.setVisible(true);
        
            btnEditar.setVisible(true);
            btnExcluir.setVisible(true);
        }
        else
        {
            JOptionPane.showMessageDialog(null, "Base vazia");
        }
    }//GEN-LAST:event_btnOptionsActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        String nome = txtEditar.getText().toUpperCase(); //variables
        int i = bditems.pesquisar(nome);
        String novoNome;
        
        if(i != -1) //checks if exists
        {
            novoNome = JOptionPane.showInputDialog("Informe o novo nome do item:");
            if(JOptionPane.showConfirmDialog(null, "Editar?", "Editar?", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION)
            {
                bditems.editar(i, novoNome); //edits value
                JOptionPane.showMessageDialog(null, "Item editado com sucesso");
                txtLista.setText(bditems.listarHards()); //list
                txtEditar.setText(null); //clears field
            }
            else
            {
                JOptionPane.showMessageDialog(null, "Edicao cancelada");
            }
        }
        else if(bditems.returnVazio()) //check if empty
        {
            JOptionPane.showMessageDialog(null, "Base vazia"); //out if empty
        }
        else
        {
            JOptionPane.showMessageDialog(null, "Item nao encontrado"); //out insucess
        }
        
        pnEdit.setVisible(false);
        
        lblEdit.setVisible(false);
        txtEditar.setVisible(false);
        
        btnEditar.setVisible(false);
        btnExcluir.setVisible(false);
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        String nome = txtEditar.getText().toUpperCase(); //variables
        int i = bditems.pesquisar(nome);
        
        if(i != -1) //checks if exists
        {
            if(JOptionPane.showConfirmDialog(null, "Excluir?", "Exclur?", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION)
            {
                bditems.excluir(i); //deletes value
                JOptionPane.showMessageDialog(null, "Item excluido com sucesso");
                txtLista.setText(bditems.listarHards()); //list
                txtEditar.setText(null); //clears field
            }
            else
            {
                JOptionPane.showMessageDialog(null, "Exclusao cancelada");
            }
        }
        else if(bditems.returnVazio()) //check if empty
        {
            JOptionPane.showMessageDialog(null, "Base vazia"); //out if empty
        }
        else
        {
            JOptionPane.showMessageDialog(null, "Item nao encontrado"); //out insucess
        }
        
        pnEdit.setVisible(false);
        
        lblEdit.setVisible(false);
        txtEditar.setVisible(false);
        
        btnEditar.setVisible(false);
        btnExcluir.setVisible(false);
    }//GEN-LAST:event_btnExcluirActionPerformed

